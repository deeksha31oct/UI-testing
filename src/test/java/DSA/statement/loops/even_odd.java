package DSA.statement.loops;

public class even_odd {
    public static void main(String[]args)
    {
        System.out.println("for even :");
        for(int i = 2 ; i<=40;i +=2 )
        {
            System.out.print( i + " ");
        }
        System.out.println(" \n for odd : ");
        for(int i = 1 ; i<= 40 ;i +=2)
        {
            System.out.print( i+ " " );
        }
    }
}

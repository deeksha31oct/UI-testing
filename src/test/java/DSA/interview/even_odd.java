public class even_odd {
    public static void main(String[]args)
    {
        System.out.println("print even number :");
        for(int i =2 ; i<=42;i +=2)
        {
            System.out.print(i+" ");
        }
        System.out.println("\n print odd number :");
        for( int i=1;i<=42 ;i+=2)
        {
System.out.print(i+" ");
        }
    }
}
